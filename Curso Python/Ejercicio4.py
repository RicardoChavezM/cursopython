# Entradas de datos
Grados= float(input('Escribe los grados que se desean convertir :'))

# Proceso de datos
Grados_Celcius= Grados
Grados_Fahrenheit= (Grados*1.8)+32
Grados_Kelvin= Grados + 273.15


# Salidas de datos
print (f'Los grados Celcius son = { Grados_Celcius }')
print (f'Los grados Fahrenheit son = { Grados_Fahrenheit }')
print (f'Los grados Kelvin son = { Grados_Kelvin }')

